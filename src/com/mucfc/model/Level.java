package com.mucfc.model;

public class Level {
	
	private String case_info;
	private String case_level;
	private int counts;
	
	public String getCase_info() {
		return case_info;
	}
	public void setCase_info(String case_info) {
		this.case_info = case_info;
	}
	public String getCase_level() {
		return case_level;
	}
	public void setCase_level(String case_level) {
		this.case_level = case_level;
	}
	public int getCounts() {
		return counts;
	}
	public void setCounts(int counts) {
		this.counts = counts;
	}
}
