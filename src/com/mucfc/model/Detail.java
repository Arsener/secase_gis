package com.mucfc.model;

public class Detail {
	private String case_name;
	private String case_describe;
	private String case_equipment;
	private String case_place;
	public String getCase_name() {
		return case_name;
	}
	public void setCase_name(String case_name) {
		this.case_name = case_name;
	}
	public String getCase_describe() {
		return case_describe;
	}
	public void setCase_describe(String case_describe) {
		this.case_describe = case_describe;
	}
	public String getCase_equipment() {
		return case_equipment;
	}
	public void setCase_equipment(String case_equipment) {
		this.case_equipment = case_equipment;
	}
	public String getCase_place() {
		return case_place;
	}
	public void setCase_place(String case_place) {
		this.case_place = case_place;
	}
	
	
}
